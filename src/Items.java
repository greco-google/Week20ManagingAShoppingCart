public class Items {

		
		private String variety;
		private int numBoxes;
		public Items(String variety, int numBoxes) {
			super();
			this.variety = variety;
			this.numBoxes = numBoxes;
		}
		
		public String getVariety() {
			return variety;
		}
		
		public int getNumBoxes() {
			return numBoxes;
		}


}