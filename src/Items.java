public class Items {

		
		private String variety;
		private int numItem;
		private int price;
		public Items(String variety, int price, int numItem) {
			super();
			this.variety = variety;
			this.price = price;
			this.numItem = numItem;
		}
		
		public String getVariety() {
			return variety;
		}
		
		public String getPrice() {
			return variety;
		}
		
		public int getnumItem() {
			return numItem;
		}


}